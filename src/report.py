        y_cursor -= 0.017 * (_line_count(finding.finding, 108) + 0.6)
        _wrapped_text(ax, _MARGIN + 0.015, y_cursor, "-> " + _wrap(finding.code_resolution, 100), fontsize=8, linespacing=1.35, color="#1f5c8b")
