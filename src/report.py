    _wrapped_text(
        ax, _MARGIN, y2,
        "Hamilton's equations give the exact force decomposition used by the optimizer:",
        fontsize=10,
    )
    ax.text(0.5, y2 - 0.075,
            r"$\dot{p} = -\nabla_\theta L(\theta) \;-\; F_{geo}(\theta, p) \;-\; F_{mem}(t) \;-\; \gamma\, p \;+\; \alpha\, \nabla_\theta S(g)$",
            ha="center", fontsize=11)

    y3 = y2 - 0.12
    rows = [
        (r"$F_{geo} = \frac{1}{2}\, p^{T}(\nabla_\theta g^{-1})\, p$",
         "Curvature correction. Exactly the Hamilton's-equation term; zero for a constant "
         "(theta-independent) metric such as this benchmark's fixed-feature Hessian, confirmed "
         "numerically to 4.6e-11 (geometric-force check)."),
        (r"$F_{mem}(t) = \sum_{k \leq t} \kappa^{t-k}\, \nabla_\theta L(\theta_k)$",
         "Exponentially-weighted gradient memory, implemented as a Markovian auxiliary variable "
         "M_t = kappa*M_{t-1} + grad L(theta_t); matches the closed form to 8.9e-16 (memory-recursion check)."),
        (r"$\gamma\, p$  (Rayleigh dissipation)",
         "Friction. This project fixes the PDF's ambiguous momentum-space damping law by defining "
         "R(theta, theta_dot) = 0.5 gamma g_ij(theta) theta_dot^i theta_dot^j in velocity space, which gives "
         "dR/d(theta_dot) = gamma*p exactly for any curved metric (rayleigh-curved-metric check, 1.6e-9)."),
        (r"$g(\theta) = H(\theta) + \lambda_{eff}\, I$",
         "Metric. The PDF's literal lambda > 0 does not guarantee positive-definiteness for an indefinite "
         "Hessian. This project's _positive_definite_metric adaptively raises lambda_eff above "
         "-lambda_min(H) (Levenberg-Marquardt style), verified on a counterexample and 200 random indefinite trials."),
        (r"$\nabla_\theta S(g)$ via Hellmann-Feynman",
         "Spectral entropy S(g) = -sum_i hat(lambda)_i ln hat(lambda)_i over normalized eigenvalues of g. The "
         "PDF states S(g) but never derives its gradient; this project derives it via eigenvalue-perturbation "
         "theory and checks it against a direct finite difference on a genuinely theta-dependent metric (1.1e-10)."),
    ]
    y_cursor = y3
    for equation, description in rows:
        ax.text(_MARGIN, y_cursor, equation, fontsize=10, va="top")
        wrapped = _wrap(description, 100)
        _wrapped_text(ax, _MARGIN + 0.02, y_cursor - 0.024, wrapped, fontsize=8.0, linespacing=1.35, color="#333333")
        y_cursor -= 0.024 + 0.0135 * _line_count(description, 100) + 0.016

    _footer(ax, "Page 2 -- Theory")
    pdf.savefig(fig)
    plt.close(fig)


