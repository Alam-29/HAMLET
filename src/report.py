def _theory_page(pdf: PdfPages) -> None:
    fig, ax = _new_page()
    y = _header(ax, "1. The Hamiltonian-Geometric Optimizer", "As implemented in src/pinn.py")

    _wrapped_text(
        ax, _MARGIN, y,
        "Parameters $\\theta$ are treated as generalized coordinates on a Riemannian manifold\n"
        "with a loss-curvature metric $g(\\theta)$, and given a canonical momentum $p$. The\n"
        "Hamiltonian is the Legendre transform of the kinetic + potential Lagrangian:",
        fontsize=10,
    )
    ax.text(0.5, y - 0.115, r"$H(\theta, p) = \frac{1}{2}\, p^{T} g^{-1}(\theta)\, p \;+\; L(\theta)$",
            ha="center", fontsize=13)

