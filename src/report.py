def _wrap(text: str, width: int) -> str:
    import textwrap

    return "\n".join(textwrap.wrap(" ".join(text.split()), width=width))


def _line_count(text: str, width: int) -> int:
    return max(1, len(_wrap(text, width).split("\n")))


def _read_summary_csv(path: Path) -> list[dict]:
    with open(path, "r", encoding="utf-8", newline="") as file:
        return list(csv.DictReader(file))


def build_pdf_report(
    output_path: str,
    project_root: Path,
    benchmark_summary_path: Path,
    potential_png: Path,
    fringing_png: Path,
    convergence_png: Path,
    spectrum_png: Path,
) -> None:
    """Assemble the full PDF report from the project's own analysis modules
    and already-generated plot/CSV artifacts. Every number is computed live;
    nothing in this function is hand-typed data."""

    numeric_checks = run_numeric_checks()
    findings = validate_literature_review_math()

    normal_mode_checks = run_normal_mode_checks()
    full_config = PINNConfig()
    full_model = FixedFeaturePotentialModel(full_config)
    full_dataset = build_pinn_dataset(full_model, full_config)
    full_modes = compute_normal_modes(full_dataset)
    conditioning = summarize_conditioning(full_modes)

    summary_rows = _read_summary_csv(benchmark_summary_path)

    with PdfPages(output_path) as pdf:
        _title_page(pdf)
        _theory_page(pdf)
        _corrections_table_page(pdf, findings)
        _numeric_checks_page(pdf, numeric_checks)
        _image_page(
            pdf,
            "Physics-Informed Benchmark: The Capacitor Fringing Field",
            "Ground-truth potential and field-line geometry the optimizers are fit against",
            potential_png,
            "2D electrostatic potential solved by the finite-difference Laplace solver, with the two "
            "parallel-plate electrodes marked. This is the target field the fixed-feature PINN and every "
            "optimizer in Sec. 6 fit least-squares against.",
            "Page 5a -- Potential field",
        )
        _image_page(
            pdf,
            "Physics-Informed Benchmark: The Capacitor Fringing Field",
            "Field lines: straight in the gap, fringed and bowing at the plate edges",
            fringing_png,
            "Streamlines seeded on the gap midline and near each plate tip, cropped to the near-plate region "
            "(matching the reference PDF's Figure 1 framing). The field is uniform and vertical between the "
            "plates and bows outward past the edges, as expected for a finite parallel-plate capacitor.",
            "Page 5b -- Fringing field",
        )
        _normal_mode_theory_page(pdf, normal_mode_checks, conditioning, full_modes.condition_number)
        _image_page(
            pdf,
            "5. Loss Hessian Spectrum and Per-Mode Convergence",
            "Production benchmark config -- condition number and per-mode contraction rate, plain vs. preconditioned",
            spectrum_png,
            "Left: the loss Hessian's eigenvalue spectrum spans roughly 9 orders of magnitude. Right: plain "
            "descent's per-mode contraction rate saturates near 1.0 (no progress) for most modes, since its "
            "single global step size is set by the stiffest mode; metric-preconditioned descent holds a "
            "roughly uniform, well-bounded contraction rate across every mode by construction.",
            "Page 5c -- Spectrum",
        )
        _image_page(
            pdf,
            "6. Optimizer Convergence",
            "Loss vs. training step, log scale, all five optimizers on the identical benchmark problem",
            convergence_png,
            "SGD and falling-ball (both unpreconditioned) barely leave their initial loss at this step budget "
            "on the severely ill-conditioned loss surface. Adam oscillates without comparable net progress. "
            "entropy_descent and hamiltonian_geometric (both metric-preconditioned) converge substantially, "
            "consistent with the conditioning-independent contraction rate proven in Sec. 4-5.",
            "Page 6a -- Convergence",
        )
        _benchmark_summary_page(pdf, summary_rows)
        _conclusion_page(pdf)
