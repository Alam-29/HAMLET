PAGE_SIZE = (8.5, 11.0)
_MARGIN = 0.07


def _new_page():
    fig = plt.figure(figsize=PAGE_SIZE)
    ax = fig.add_axes((0.0, 0.0, 1.0, 1.0))
    ax.set_xlim(0.0, 1.0)
    ax.set_ylim(0.0, 1.0)
    ax.axis("off")
    return fig, ax


def _header(ax, title: str, subtitle: str = "") -> float:
    ax.text(_MARGIN, 0.955, title, fontsize=16, fontweight="bold", va="top")
    y = 0.93
    if subtitle:
        ax.text(_MARGIN, y, subtitle, fontsize=10, va="top", color="#444444")
        y -= 0.025
    ax.plot([_MARGIN, 1 - _MARGIN], [y, y], color="#999999", linewidth=0.8)
    return y - 0.03


def _wrapped_text(ax, x: float, y: float, text: str, fontsize: float = 9.5, linespacing: float = 1.5, **kwargs) -> None:
    ax.text(x, y, text, fontsize=fontsize, va="top", ha="left", linespacing=linespacing, **kwargs)


def _footer(ax, page_label: str) -> None:
    ax.text(0.5, 0.015, page_label, fontsize=8, color="#999999", ha="center")


